
//create a function called: countOnly
// will take in an array(allItems) and an object(itemsToCount)
// return an object containing counts of everythin input object listed
//only truthy values should be counted
//

const countOnly = function(allItems, itemsToCount) {
  const results = {};
  for (const item of allItems) {
    if (itemsToCount[item]) {
      if (results[item]) {
        results[item] += 1;
      } else {
        results[item] = 1;
      }
    }
  }
  return results;
};

